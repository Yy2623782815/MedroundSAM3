import os
import sys
import types
from pathlib import Path
from typing import Any, Dict, List, Optional

import numpy as np
import torch
import torch.nn.functional as F
from PIL import Image as PILImage
from torchvision.ops import nms


def _ensure_path(p: str) -> None:
    pp = str(Path(p).resolve())
    if pp not in sys.path:
        sys.path.insert(0, pp)


def _ensure_hf_stub_when_local_only(load_from_hf: bool) -> None:
    if load_from_hf:
        return
    if "huggingface_hub" in sys.modules:
        return

    stub = types.ModuleType("huggingface_hub")

    def _hf_hub_download(*args: Any, **kwargs: Any) -> str:
        raise RuntimeError(
            "huggingface_hub is not installed and load_from_hf=False is expected. "
            "Please provide local checkpoint_path/bpe_path."
        )

    stub.hf_hub_download = _hf_hub_download
    sys.modules["huggingface_hub"] = stub


def _extract_lora_ckpt_state(ckpt_obj: Dict[str, Any]) -> Dict[str, torch.Tensor]:
    if "lora_state_dict" in ckpt_obj and isinstance(ckpt_obj["lora_state_dict"], dict):
        return ckpt_obj["lora_state_dict"]

    tensor_values = all(torch.is_tensor(v) for v in ckpt_obj.values())
    if tensor_values:
        return ckpt_obj

    raise KeyError("Could not find LoRA weights. Expected key 'lora_state_dict' or plain tensor state dict.")


class MedSAM3MyLoRAInferencer:
    """
    Inference wrapper for work/medsam3_my_lora checkpoints.

    Key differences from original medsam3_lora_gt_label_eval:
    - LoRA layer injection/loading uses work/medsam3_my_lora/models/lora.py
    - checkpoint format supports best.pt generated by train_medsam3_my_lora.py
    """

    def __init__(
        self,
        *,
        sam3_repo_root: str,
        my_lora_project_root: str,
        lora_checkpoint_path: str,
        checkpoint_path: str,
        bpe_path: str,
        resolution: int = 1008,
        detection_threshold: float = 0.5,
        nms_iou_threshold: float = 0.5,
        device: str = "cuda",
        load_from_hf: bool = False,
        lora_r: Optional[int] = None,
        lora_alpha: Optional[int] = None,
        lora_dropout: Optional[float] = None,
    ):
        if not os.path.exists(lora_checkpoint_path):
            raise FileNotFoundError(f"LoRA checkpoint not found: {lora_checkpoint_path}")

        _ensure_path(sam3_repo_root)
        _ensure_path(my_lora_project_root)
        _ensure_hf_stub_when_local_only(load_from_hf=load_from_hf)

        from sam3.model_builder import build_sam3_image_model
        from sam3.train.data.sam3_image_dataset import (
            Datapoint,
            FindQueryLoaded,
            Image as SAMImage,
            InferenceMetadata,
        )
        from sam3.train.data.collator import collate_fn_api
        from sam3.model.utils.misc import copy_data_to_device
        from sam3.train.transforms.basic_for_api import ComposeAPI, NormalizeAPI, RandomResizeAPI, ToTensorAPI

        from models.lora import LoRAConfig, inject_lora_into_model, load_lora_state_dict

        self.Datapoint = Datapoint
        self.FindQueryLoaded = FindQueryLoaded
        self.SAMImage = SAMImage
        self.InferenceMetadata = InferenceMetadata
        self.collate_fn_api = collate_fn_api
        self.copy_data_to_device = copy_data_to_device

        self.resolution = int(resolution)
        self.detection_threshold = float(detection_threshold)
        self.nms_iou_threshold = float(nms_iou_threshold)
        self.device = torch.device(device if torch.cuda.is_available() else "cpu")

        print("[MedSAM3-MyLoRA] Initializing SAM3 + My LoRA")
        print(f"[MedSAM3-MyLoRA] device={self.device}")
        print(f"[MedSAM3-MyLoRA] resolution={self.resolution}")
        print(f"[MedSAM3-MyLoRA] detection_threshold={self.detection_threshold}")
        print(f"[MedSAM3-MyLoRA] nms_iou_threshold={self.nms_iou_threshold}")
        print(f"[MedSAM3-MyLoRA] load_from_hf={load_from_hf}")

        ckpt_obj = torch.load(lora_checkpoint_path, map_location="cpu")
        lora_state = _extract_lora_ckpt_state(ckpt_obj)

        if isinstance(ckpt_obj, dict):
            cfg_lora = ckpt_obj.get("config", {}).get("lora", {})
            if lora_r is None:
                lora_r = cfg_lora.get("r")
            if lora_alpha is None:
                lora_alpha = cfg_lora.get("alpha")
            if lora_dropout is None:
                lora_dropout = cfg_lora.get("dropout")

        lora_r = int(8 if lora_r is None else lora_r)
        lora_alpha = int(16 if lora_alpha is None else lora_alpha)
        lora_dropout = float(0.05 if lora_dropout is None else lora_dropout)

        self.model = build_sam3_image_model(
            bpe_path=bpe_path,
            checkpoint_path=checkpoint_path,
            load_from_HF=load_from_hf,
            device=self.device.type,
            eval_mode=True,
            compile=False,
        )

        lora_cfg = LoRAConfig(r=lora_r, alpha=lora_alpha, dropout=lora_dropout)
        replaced = inject_lora_into_model(model=self.model, cfg=lora_cfg, verbose=True)
        print(f"[MedSAM3-MyLoRA] replaced_linear_layers={len(replaced)}")

        load_lora_state_dict(self.model, lora_state, strict=True)

        self.model.to(self.device)
        self.model.eval()

        self.transform = ComposeAPI(
            transforms=[
                RandomResizeAPI(
                    sizes=self.resolution,
                    max_size=self.resolution,
                    square=True,
                    consistent_transform=False,
                ),
                ToTensorAPI(),
                NormalizeAPI(mean=[0.5, 0.5, 0.5], std=[0.5, 0.5, 0.5]),
            ]
        )

    def create_datapoint(self, pil_image: PILImage.Image, text_prompts: List[str]):
        w, h = pil_image.size
        sam_image = self.SAMImage(data=pil_image, objects=[], size=[h, w])

        queries = []
        for idx, text_query in enumerate(text_prompts):
            query = self.FindQueryLoaded(
                query_text=text_query,
                image_id=0,
                object_ids_output=[],
                is_exhaustive=True,
                query_processing_order=idx,
                inference_metadata=self.InferenceMetadata(
                    coco_image_id=idx,
                    original_image_id=idx,
                    original_category_id=1,
                    original_size=[w, h],
                    object_id=0,
                    frame_index=0,
                ),
            )
            queries.append(query)

        return self.Datapoint(find_queries=queries, images=[sam_image])

    @torch.no_grad()
    def predict_single_prompt(self, image_path: str, prompt: str) -> dict:
        if not os.path.exists(image_path):
            raise FileNotFoundError(f"Image not found: {image_path}")

        pil_image = PILImage.open(image_path).convert("RGB")
        orig_w, orig_h = pil_image.size

        datapoint = self.create_datapoint(pil_image, [prompt])
        datapoint = self.transform(datapoint)
        batch = self.collate_fn_api([datapoint], dict_key="input")["input"]
        batch = self.copy_data_to_device(batch, self.device, non_blocking=True)

        outputs = self.model(batch)
        last_output = outputs[-1]

        pred_logits = last_output["pred_logits"]  # [B, Q, C]
        pred_boxes = last_output["pred_boxes"]  # [B, Q, 4]
        pred_masks = last_output.get("pred_masks", None)  # [B, Q, H, W]

        out_probs = pred_logits.sigmoid()
        scores = out_probs[0, :, :].max(dim=-1)[0]

        keep = scores > self.detection_threshold
        num_keep = int(keep.sum().item())

        if num_keep == 0:
            return {
                "original_image_path": image_path,
                "orig_img_h": orig_h,
                "orig_img_w": orig_w,
                "pred_boxes": [],
                "pred_scores": [],
                "pred_masks": [],
                "num_detections": 0,
            }

        boxes_cxcywh = pred_boxes[0, keep]
        kept_scores = scores[keep]

        cx, cy, w, h = boxes_cxcywh.unbind(-1)
        x1 = (cx - w / 2) * orig_w
        y1 = (cy - h / 2) * orig_h
        x2 = (cx + w / 2) * orig_w
        y2 = (cy + h / 2) * orig_h
        boxes_xyxy = torch.stack([x1, y1, x2, y2], dim=-1)

        keep_nms = nms(boxes_xyxy, kept_scores, self.nms_iou_threshold)
        boxes_xyxy = boxes_xyxy[keep_nms]
        kept_scores = kept_scores[keep_nms]

        masks_np = []
        if pred_masks is not None:
            masks_small = pred_masks[0, keep][keep_nms].sigmoid() > 0.5
            masks_resized = F.interpolate(
                masks_small.unsqueeze(0).float(),
                size=(orig_h, orig_w),
                mode="bilinear",
                align_corners=False,
            ).squeeze(0) > 0.5
            masks_np = masks_resized.cpu().numpy().astype(np.uint8)

        pred_boxes_xywh_norm = []
        for box in boxes_xyxy.cpu().numpy():
            x1_, y1_, x2_, y2_ = box.tolist()
            bw = max(0.0, x2_ - x1_)
            bh = max(0.0, y2_ - y1_)
            pred_boxes_xywh_norm.append([x1_ / orig_w, y1_ / orig_h, bw / orig_w, bh / orig_h])

        pred_scores_list = kept_scores.cpu().numpy().tolist()
        order = sorted(range(len(pred_scores_list)), key=lambda i: pred_scores_list[i], reverse=True)

        pred_scores_list = [pred_scores_list[i] for i in order]
        pred_boxes_xywh_norm = [pred_boxes_xywh_norm[i] for i in order]

        if isinstance(masks_np, np.ndarray) and masks_np.ndim == 3 and len(order) > 0:
            masks_np = masks_np[order]
        elif isinstance(masks_np, list) and len(order) > 0:
            masks_np = [masks_np[i] for i in order]

        print(
            f"[MedSAM3-MyLoRA] prompt={prompt} "
            f"num_detections={len(pred_scores_list)} top_scores={pred_scores_list[:5]}"
        )

        return {
            "original_image_path": image_path,
            "orig_img_h": orig_h,
            "orig_img_w": orig_w,
            "pred_boxes": pred_boxes_xywh_norm,
            "pred_scores": pred_scores_list,
            "pred_masks": masks_np,
            "num_detections": int(len(pred_scores_list)),
        }
